WHERE MH.MAMH IN 
(	
	SELECT MAMH_TRUOC FROM DIEUKIEN
	WHERE DIEUKIEN.MAMH IN (SELECT MAMH FROM MONHOC WHERE TENMH = 'Co so du lieu')
)

