SELECT TENMH FROM MONHOC AS MH
							INNER JOIN GIANGDAY AS GD ON MH.MAMH = GD.MAMH
							INNER JOIN GIAOVIEN AS GV ON GV.MAGV = GD.MAGV
WHERE GD.HOCKY = 1 AND GD.NAM = 2006 AND GV.HOTEN = 'Tran Tam Thanh'
GROUP BY TENMH

